

public class Casting {

	public static void main(String[] args)
	{
		int i=29;
		System.out.println(i);
		double j= i;
		System.out.println(j);
		float f=i;
		System.out.println(f);
		byte b=(byte)i;
		System.out.println(b);
		String s="121";
		int k=Integer.parseInt(s);
		System.out.println(k-9+4*5/6);
		
	}
}

